class Ascii
{
	public static void main(String[] args) 
	{
	    
		int n = 65;
		for(char i = 'A' ; i <= 'Z' ; i++){
			System.out.println(i + "--->" + n);
			n++;
		}
		int m = 97;
		for(char i = 'a' ; i <= 'z' ; i++){
			System.out.println(i + "--->" + m);
			m++;
		}
		
		

  }
}
